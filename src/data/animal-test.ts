export function calculateAnimalScores(answers: Record<string, string>): AnimalScores {
  const scores: AnimalScores = {
    fox: 0,
    wolf: 0,
    cat: 0,
    dog: 0,
    bear: 0,
    rabbit: 0,
    owl: 0,
    dolphin: 0
  };

  // ê° ì§ˆë¬¸ì— ëŒ€í•œ ë‹µë³€ì„ ìˆœíšŒí•˜ë©° ì ìˆ˜ ê³„ì‚°
  Object.entries(answers).forEach(([questionId, answerId]) => {
    const question = animalQuestions.find(q => q.id === questionId);
    if (question) {
      const option = question.options.find(o => o.id === answerId);
      if (option) {
        option.traits.forEach(trait => {
          if (trait in scores) {
            scores[trait as keyof AnimalScores] += 1;
          }
        });
      }
    }
  });

  return scores;
}

